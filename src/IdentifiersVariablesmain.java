/*All Java variables have a unique name
 * variable name is case sensitive ("myVar" and "myvar" are different variables)
 * Variable name is should start with lowercase latter.*/
public class IdentifiersVariablesmain {
	public static void main(String[]args) {
		
		System.out.println(" * All Java variables have a unique name\r\n"
				+ " * variable name is case sensitive (\"myVar\" and \"myvar\" are different variables)\r\n"
				+ " * Variable name is should start with lowercase latter.");
		//This type of variable declaration is good.
		int minutsPerHour = 60;
		
		System.out.println(minutsPerHour);
		
		//ok but not so easy to understand what m actually is
		
		int m = 60;
		
		
		System.out.println(m);
		
	}
	

}
